     * @return \Illuminate\Http\Response
     */
    public function update(Request $data, $id)
    {
        $user = User::where('id', '=', $id)->update([
            'name' => $data['name'],
            'email' => $data['email'],
            'password' => Hash::make($data['password']),
        ]);

        if ($user) return back()->with('mensagem', 'Usuario editado com sucesso');
        else return back()->with('mensagem', 'Erro ao editar usuario');

    }

